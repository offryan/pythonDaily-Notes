
v1 = float(input ("Digite o primeiro número: "))
v2 = float(input ("Digite o segundo número: "))
soma =  v1 + v2
subtracao = v1 - v2
multiplicacao = v1 * v2
divisao = v1 / v2
resto = v1 % v2

print(f"{v1} + {v2} = {soma}")
print(f"{v1} - {v2} = {subtracao}")
print(f"{v1} * {v2} = {multiplicacao}")
print(f"{v1} / {v2} = {divisao}")
print(f"{v1} % {v2} = {resto}")
print(f"O resto da divisão de {v1} por {v2} é {resto}")
